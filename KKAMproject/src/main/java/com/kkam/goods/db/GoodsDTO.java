package com.kkam.goods.db;

public class GoodsDTO {
	private String G_code;
	private String G_product;
	private String G_img;
	private String G_desc;
	private String G_stop;
	private int G_price;
	
	public String getG_code() {
		return G_code;
	}
	public void setG_code(String g_code) {
		G_code = g_code;
	}
	public String getG_product() {
		return G_product;
	}
	public void setG_product(String g_product) {
		G_product = g_product;
	}
	public String getG_img() {
		return G_img;
	}
	public void setG_img(String g_img) {
		G_img = g_img;
	}
	public String getG_desc() {
		return G_desc;
	}
	public void setG_desc(String g_desc) {
		G_desc = g_desc;
	}
	public String getG_stop() {
		return G_stop;
	}
	public void setG_stop(String g_stop) {
		G_stop = g_stop;
	}
	public int getG_price() {
		return G_price;
	}
	public void setG_price(int g_price) {
		G_price = g_price;
	}
}
